package com.gzw.mp.bean;

/**用户排行榜
 * coder by 背离记 on 2015/11/10.
 */
public class TopUsersBean {
    private String rank;
    private String suggestion_reason;
    private User user;

    /**序号吧，0,1,2,3
     * @return
     */
    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    /**一句话推荐原因
     * @return
     */
    public String getSuggestion_reason() {
        return suggestion_reason;
    }

    public void setSuggestion_reason(String suggestion_reason) {
        this.suggestion_reason = suggestion_reason;
    }

    /**活动用户对象
     * @return
     */
    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
