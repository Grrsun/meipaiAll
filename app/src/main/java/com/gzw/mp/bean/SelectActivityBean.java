package com.gzw.mp.bean;

/**活动精选豆子类
 * coder by 背离记 on 2015/11/12.
 */
public class SelectActivityBean {
    /**
     * 这个话题（活动）热门吗
     */
    private boolean has_hot_feature;
    /**
     * 这个话题（活动）是父话题吗
     */
    private boolean is_parent;
    /**
     * 话题（活动）名字
     */
    private String name;
    /**
     * 话题（活动）图片
     */
    private String picture;
    /**
     * 话题ID
     */
    private String topic_id;

    /**
     * 这个话题（活动）热门吗
     */
    public boolean isHas_hot_feature() {
        return has_hot_feature;
    }

    public void setHas_hot_feature(boolean has_hot_feature) {
        this.has_hot_feature = has_hot_feature;
    }

    /**
     * 这个话题（活动）是父话题吗
     */
    public boolean is_parent() {
        return is_parent;
    }

    public void setIs_parent(boolean is_parent) {
        this.is_parent = is_parent;
    }

    /**
     * 话题（活动）名字
     */
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    /**
     * 话题（活动）图片
     */
    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    /**
     * 话题ID
     */
    public String getTopic_id() {
        return topic_id;
    }

    public void setTopic_id(String topic_id) {
        this.topic_id = topic_id;
    }
}
